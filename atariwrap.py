### Encode an Atari environment with a pre-trained model
###
class EncodedAtari(gym.ObservationWrapper):
    def __init__(self, name):
        # Make the Atari environment
        env = make_atari(name)
        env = wrap_deepmind(env)
        env = wrap_pytorch(env)

        super(EncodedAtari, self).__init__(env)

        # Model used to encode frames
        model = CnnDQN(env.observation_space.shape, env.action_space.n)
        model.load_state_dict(torch.load(name + '.weights', map_location='cpu'))

        self.model = model
        self.observation_space = gym.spaces.Box(low=0.0, high=10.0, shape=(model.feature_size(),), dtype=np.float32)

    def observation(self, observation):
        # Pass the observation through the encoder
        with torch.no_grad():
            rs = self.model.features(torch.FloatTensor(np.float32(observation)).unsqueeze(0))

        return rs.cpu().numpy().ravel()

